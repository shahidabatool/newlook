export const getPracticeQuestions = (moduleId: string | number): Question[] => {
  // Convert moduleId to string for comparison
  const moduleIdStr = moduleId.toString();
  
  // If it's a main module (1-11)
  if (!moduleIdStr.includes('.')) {
    return questionBank.filter(q => q.chapter === moduleIdStr.padStart(2, '0'));
  }
  
  // If it's a submodule (e.g., 11.1)
  const [mainModule, subModule] = moduleIdStr.split('.');
  const mainModuleQuestions = questionBank.filter(q => q.chapter === mainModule.padStart(2, '0'));
  
  // For submodules, return a subset of the main module questions
  const questionsPerSubmodule = Math.floor(mainModuleQuestions.length / 5); // 5 submodules
  const subModuleIndex = parseInt(subModule) - 1;
  return mainModuleQuestions.slice(
    subModuleIndex * questionsPerSubmodule,
    (subModuleIndex + 1) * questionsPerSubmodule
  );
};

export const getTestQuestions = (): Question[] => {
  // Get 20 random questions from all chapters
  const shuffled = [...questionBank].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, 20);
};

export const getMockExamQuestions = (): Question[] => {
  // Get 20 random questions but ensure coverage of all main topics
  const mockExamQuestions: Question[] = [];
  const chaptersNeeded = new Set(['01', '02', '03', '04', '05', '06', '07', '08', '09', '10', '11']);
  
  // First, get one question from each chapter
  chaptersNeeded.forEach(chapter => {
    const chapterQuestions = questionBank.filter(q => q.chapter === chapter);
    if (chapterQuestions.length > 0) {
      const randomQuestion = chapterQuestions[Math.floor(Math.random() * chapterQuestions.length)];
      mockExamQuestions.push(randomQuestion);
    }
  });
  
  // Then fill the remaining slots with random questions
  const remainingQuestions = [...questionBank]
    .filter(q => !mockExamQuestions.includes(q))
    .sort(() => 0.5 - Math.random());
  
  while (mockExamQuestions.length < 20 && remainingQuestions.length > 0) {
    mockExamQuestions.push(remainingQuestions.pop()!);
  }
  
  return mockExamQuestions.sort(() => 0.5 - Math.random());
};